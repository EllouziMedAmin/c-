#include<iostream>
#include<cmath>

using namespace std;

int main()
{
    int x=4;
    int y=7;
    cout << "the maximum "<< max(x,y)<<endl;
    cout<<"square root 4 = "<<sqrt(4)<<endl;
    cout<< "2^4 = "<< pow(2,4) << endl;
    cout <<"round ( normal) = "<< round(3.14)<<endl;
    cout <<"round up = "<< ceil(3.0001)<<endl;
    cout <<"round down = "<< floor(3.99)<<endl;


 


    return 0;
}